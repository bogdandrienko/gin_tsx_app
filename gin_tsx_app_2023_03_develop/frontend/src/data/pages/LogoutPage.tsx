import React, { useEffect } from "react";
import { useDispatch } from "react-redux";
import { useNavigate } from "react-router-dom";

// TODO custom modules /////////////////////////////////////////////////////////////////////////////////////////////////

import * as base from "../components/ui/base";
import * as utils from "../components/utils";
import * as slices from "../components/slices";

// TODO export /////////////////////////////////////////////////////////////////////////////////////////////////////////

export default function Page(): JSX.Element {
  // TODO store ////////////////////////////////////////////////////////////////////////////////////////////////////////

  // TODO hooks ////////////////////////////////////////////////////////////////////////////////////////////////////////

  const dispatch = useDispatch();
  const navigate = useNavigate();

  // TODO functions ////////////////////////////////////////////////////////////////////////////////////////////////////

  useEffect(() => {
    utils.deleteCookie("userAccessToken");
    utils.deleteCookie("userRefreshToken");

    // @ts-ignore
    dispatch({ type: slices.user.userLoginStore.constant.reset });

    utils.Delay(() => navigate("/"), 100);
  }, []);

  // TODO return ///////////////////////////////////////////////////////////////////////////////////////////////////////
  return (
    <base.Base1 title={"Logout page"} description={"logout from your account"}>
      .
    </base.Base1>
  );
}
